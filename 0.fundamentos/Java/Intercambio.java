public class Intercambio {
      public static void main(String[] args) {

          //Declarar variables
          int x, z, a;

          //Leer datos
          x = 5;
          z = 2;

          //Procesos
          a = x;
          x = z;
          z = a;

          //Impresion de resultados
          System.out.println("El valor de x es:" + x);
          System.out.println("El valor de z es:" + z);

    }
}
